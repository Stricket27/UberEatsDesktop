    //IMPRIMIR LOS 3 MEJORES RESTAURANTES CON MAYOR PUNTUACION
    public String imprimirPosicion() {
        String posicion = "";
        Restaurante mayor = listaRestaurante.get(0);
        for (Restaurante restaurante : listaRestaurante) {
            if (restaurante.retornaPromedioPuntuado() > mayor.retornaPromedioPuntuado()) {
                mayor = restaurante;
            }

        }
        posicion = mayor.getNombre() + " - " + mayor.retornaPromedioPuntuado() + " âœª ";
        ArrayList<Restaurante> listaModificada = new ArrayList<>();
        for (Restaurante restaurante : listaRestaurante) {
            if (!restaurante.getCodigo().equals(mayor.getCodigo())) {
                listaModificada.add(restaurante);
            }
        }
        listaRestaurante = listaModificada;
        return posicion;
    }


    public void cargarDatos() {
        this.jCboPerfil.setSelectedIndex(-1);

        for (Perfil perfil : Perfil.values()) {
            this.jCboPerfil.addItem(perfil);
        }

        this.jCboTipoTarjeta.setSelectedIndex(-1);

        for (TipoTarjeta tipo : TipoTarjeta.values()) {
            this.jCboTipoTarjeta.addItem(tipo);
        }

        this.jCboPais.setSelectedIndex(-1);

        for (PaisOrigen pais : PaisOrigen.values()) {
            this.jCboPais.addItem(pais);
        }
    }


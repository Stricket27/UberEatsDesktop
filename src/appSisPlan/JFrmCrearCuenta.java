    private void jBtnCrearCuentaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnCrearCuentaActionPerformed

        //VALIDAR QUE SI EXISTE EL CODIGO DE USUARIO
        try {
            if (UsuarioDAL.codigoVerificarUsuario(this.jTxtCodigoUsuario.getText())) {
                JOptionPane.showMessageDialog(this, "Ya existe");
                return;
            }

            //METODO PARA AGREGAR LOS DATOS DE LA TARJETA
            oTarjeta = new Tarjeta();
            oTarjeta.setNumero(this.jTxtNumeroTarjeta.getText());
            oTarjeta.setMesVencimiento(Integer.parseInt(this.jTxtMesVencimiento.getText()));
            oTarjeta.setAnnoVencimiento(Integer.parseInt(this.jTxtAnioVencimiento.getText()));
            oTarjeta.setCodigo(this.jTxtCodigoSeguriad.getText());
            oTarjeta.setTipoTarjeta((TipoTarjeta) this.jCboTipoTarjeta.getSelectedItem());

            int digitoValidado = 0;
            
            //VALIDACIONES
            if (oTarjeta.getTipoTarjeta() == TipoTarjeta.MASTER_CARD) {
                digitoValidado = Integer.parseInt(oTarjeta.getNumero().substring(0, 2));
            } else {
                digitoValidado = Integer.parseInt(oTarjeta.getNumero().substring(0, 1));
            }
            if (oTarjeta.validarTarjeta() && oTarjeta.validarFecha() && oTarjeta.validarTipoTarjeta(digitoValidado)) {
                JOptionPane.showMessageDialog(this, "Tarjeta valida");      
            } else {
                JOptionPane.showMessageDialog(this, "Tarjeta invalida");
                return;
            }
            //VALIDAR QUE NINGUN CLIENTE REGULAR O GERENTE PUEDA CREARSE UN USUARIO ADMINISTRADOR
            if (this.jCboPerfil.equals(Perfil.ADMINISTRADOR)){
                JOptionPane.showMessageDialog(this, "Lo sentimos, pero esta opcion de ADMINISTRADOR no esta disponible");
            }

            //AGREGAR DATOS DEL USUARIO
            oUsuario = new Usuario();
            oUsuario.setCodigo(this.jTxtCodigoUsuario.getText());
            oUsuario.setNombre(this.jTxtNombreCompleto.getText());
            oUsuario.setCorreoElectronico(this.jTxtCorreoElectronico.getText());
            oUsuario.setContrasenia(this.jPswContrasenia.getText());
            oUsuario.setPais((PaisOrigen) this.jCboPais.getSelectedItem());
            oUsuario.setTarjeta(oTarjeta);
            oUsuario.setPerfil((Perfil) this.jCboPerfil.getSelectedItem());

            UsuarioDAL.insertaUsuario(oUsuario);
            TarjetaDAL.insertaTarjeta(oTarjeta, oUsuario);
            JOptionPane.showMessageDialog(this, "Creado exitosamente");
            
            //VACIAR LOS DATOS PERSONALES
            this.jTxtNombreCompleto.setText("");
            this.jTxtCorreoElectronico.setText("");
            this.jPswContrasenia.setText("");
            this.jCboPais.setSelectedIndex(-1);
            this.jCboPerfil.setSelectedIndex(-1);
            this.jTxtCodigoUsuario.setText("");
            
            //VACIAR LOS DATOS DE LA TARJETA
            this.jTxtNumeroTarjeta.setText("");
            this.jCboTipoTarjeta.setSelectedIndex(-1);
            this.jTxtMesVencimiento.setText("");
            this.jTxtAnioVencimiento.setText("");
            this.jTxtCodigoSeguriad.setText("");
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.toString());
        }

    }//GEN-LAST:event_jBtnCrearCuentaActionPerformed


    //IMPRIMIR LOS DETALLES QUE REALIZO EL CLIENTE SEMEJANTE A LA FACTURA ELECTRONICA
    public void LLenarProductoEImprimirDetalle() {
        StringBuilder detalle = new StringBuilder();
        OrdenCompra ordenDetalle = (OrdenCompra) jcboOrdenes.getSelectedItem();
        detalle.append("Restaurante: ").append(ordenDetalle.getRestaurante().getNombre()).append("\n");
        jcboProductos.removeAllItems();
        for (LineaDetalle linea : ordenDetalle.getArraLineaDetalle()) {
            detalle.append(linea.toString()).append("\n\n\n");
            jcboProductos.addItem(linea.getoProducto());
        }
        jcboProductos.setSelectedIndex(-1);
        jTextArea1.setText(detalle.toString());
    }

    //LLENAR LAS FACTURAS EN UN TXTAREA PARA QUE VEA EL CLIENTE LAS COMPRAS QUE A REALIZADO
    public void llenarFacturas() {
        try {
            ArrayList<OrdenCompra> ordenes = OrdenCompraDAL.listaFacturas(usuarioActivo.getCodigo());
            for (OrdenCompra orden : ordenes) {
                orden.setCliente(usuarioActivo);
                jcboOrdenes.addItem(orden);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.toString());
        }
    }
    
    //LENAR LA PUNTACION DE LOS CLIENTES QUE LO A CALIFICADO CON UN FOR EACH
    public void llenarPuntuacion() {
        for (int i = 1; i <= 5; i++) {
            jcboCalificacion.addItem(i);
        }
        jcboCalificacion.setSelectedIndex(-1);
    }


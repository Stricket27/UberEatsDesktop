    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        //VALIDAR QUE TODO NO ESTE EN BLANCO
        if (jcboCalificacion.getSelectedIndex() == -1 || jcboOrdenes.getSelectedIndex() == -1 || jcboProductos.getSelectedIndex() == 1) {
            JOptionPane.showMessageDialog(this, "Por favor selecciones orden de compra, producto y calificación.");
            return;
        }
        
        //METODO PARA CALIFICAR EL RESTAURANTE
        try {
            RestauranteDAL.insertaCalificacionRestaurante(((OrdenCompra) jcboOrdenes.getSelectedItem()).getRestaurante(),
                     (int) jcboCalificacion.getSelectedItem());
            jcboProductos.removeItemAt(jcboProductos.getSelectedIndex());
            JOptionPane.showMessageDialog(this, "Gracias por tu calificación.");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, e.toString());
        }

    }//GEN-LAST:event_jButton1ActionPerformed

